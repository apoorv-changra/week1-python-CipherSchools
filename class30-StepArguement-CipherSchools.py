#syntax - [start arguement: stop arguement:step]
print("Apoorv"[2:5])     #output= oor
print("Apoorv"[2:5:1])   #output= oor
print("Apoorv"[2:5:2])   #output= or  
print("Apoorv"[0::2])    #output= aor
print("Apoorv"[8::-1])   #output = vroopA
print("Apoorv"[::-1])    #output = vroopA (TRICK)